package io.github.pnoker.common.driver.event.metadata;

import io.github.pnoker.common.entity.event.MetadataEvent;
import io.github.pnoker.common.enums.MetadataOperateTypeEnum;
import io.github.pnoker.common.enums.MetadataTypeEnum;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.context.ApplicationEventPublisher;

import static org.mockito.Mockito.verify;

@ExtendWith(MockitoExtension.class)
class MetadataEventPublisherTest {

    @Mock
    private ApplicationEventPublisher applicationEventPublisher;

    @Test
    void publishEventForwardsToSpringContext() {
        MetadataEventPublisher publisher = new MetadataEventPublisher(applicationEventPublisher);
        MetadataEvent event = new MetadataEvent(this, 10L, MetadataTypeEnum.DEVICE, MetadataOperateTypeEnum.UPDATE);
        publisher.publishEvent(event);
        verify(applicationEventPublisher).publishEvent(event);
    }
}
